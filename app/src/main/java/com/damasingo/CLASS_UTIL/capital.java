package com.damasingo.CLASS_UTIL;

public class capital {
    String name,lien;

    public capital(String name, String lien) {
        this.name = name;
        this.lien = lien;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLien() {
        return lien;
    }

    public void setLien(String lien) {
        this.lien = lien;
    }
}
